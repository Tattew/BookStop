    private void LoginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginBtnMouseClicked
        if(UnameTb.getText().isEmpty() || PassTb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Enter the Username and Password");
        }else if(RoleCb.getSelectedIndex()==1)
        {
            String Query = "select * from User1.USERTBL where Uname='"+UnameTb.getText()+"' and Upassword='"+PassTb.getText()+"'";
            String UserName = UnameTb.getText();
            try {
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/BookStore", "User1", "1234");
                St = Con.createStatement();
                Rs = St.executeQuery(Query);
                if(Rs.next()){
                    new bill(UserName).setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Wrong User Name or Password");
                     UnameTb.setText("");
                     PassTb.setText("");
                }
                
            } catch (Exception e) {
            }
        }else{
            if(UnameTb.getText().equals("Admin") && PassTb.getText().equals("password")){
                new BooksInterface().setVisible(true);
                this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Contact The Admin");
            UnameTb.setText("");
            PassTb.setText("");
            }
        }
    }//GEN-LAST:event_LoginBtnMouseClicked

